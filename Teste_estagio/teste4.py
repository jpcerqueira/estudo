# a) 1, 3, 5, 7, ___
# b) 2, 4, 8, 16, 32, 64, ____
# c) 0, 1, 4, 9, 16, 25, 36, ____
# d) 4, 16, 36, 64, ____
# e) 1, 1, 2, 3, 5, 8, ____
# f) 2, 10, 12, 16, 17, 18, 19, ____

##Resposta
# a) 1, 3, 5, 7, "9" // PA razão 2
# b) 2, 4, 8, 16, 32, 64, "128" // PG razão 2
# c) 0, 1, 4, 9, 16, 25, 36, "49" // Soma com impares 1 3 5 7 9 11 13
# d) 4, 16, 36, 64, "100" // 12-20-28-36 // 16-4 = 12 /  36-16 = 20 / 64-36 = 28 / 100 - 64 = 36 #a soma das subtrações tinha uma razão de 8
# e) 1, 1, 2, 3, 5, 8, "13" // Sequencia de Fibonacci
# f) 2, 10, 12, 16, 17, 18, 19, "200" // todos os números começam com a letra D